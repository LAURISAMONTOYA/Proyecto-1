            //new Integer(numeroSpinner.getValue().toString()), nombreTxt.getText()
            Usuario canal = new Usuario(new Integer(numeroSpinner.getValue().toString()), cedulaTxt.getText(), nombreTxt.getText(), telefonoTxt.getText(), direccionTxt.getText(), p.getId());
