    public void leerPaquetes() throws SQLException, ClassNotFoundException {

        ArrayList<Paquete> paquetes = new DAO().obtenerPaquetes();

        for (Paquete paquete : paquetes) {
            paquetesCbx.addItem(paquete);
        }

    }

    public void llenarTabla() throws SQLException, ClassNotFoundException {
        ArrayList<Usuario> canales = new DAO().obtenerUsuarios();
        String[] cols = {"Codigo", "Nombre", "Cedula","Telefono","Direccion", "Id Paquete"};
        Object[][] data = new Object[canales.size()][6];
        int i = 0;
        for (Usuario canal : canales) {
            data[i][0] = canal.getCodigo();
            data[i][1] = canal.getNombre();
            data[i][2] = canal.getCedula();
            data[i][3] = canal.getTelefono();
            data[i][4] = canal.getDireccion();
            data[i][5] = canal.getIdPaquete();
            i++;
        }
        tabla.setModel(new DefaultTableModel(data, cols));
    }

